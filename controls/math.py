"""Performs main math operations"""

def add(n_1: int | float, n_2: int | float) -> int | float:
    """Returns the sum of 2 numbers"""
    return n_1 + n_2

def diff(n_1: int | float, n_2: int | float) -> int | float:
    """Returns the diff of 2 numbers"""
    return n_1 - n_2

def fois(n_1: int | float, n_2: int | float) -> int | float:
    """Returns the product of 2 numbers"""
    return n_1 * n_2

def divide(n_1: int | float, n_2: int | float) -> float | float:
    """Returns the quotient of 2 numbers"""
    return n_1 / n_2
